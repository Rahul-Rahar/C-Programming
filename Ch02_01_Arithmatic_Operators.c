#include <stdio.h>

int main() {

  // Arithmetic Operators
  // Arithmetic operators used for Calculating basic Mathematical Operations

  int a = 56;
  int b = 28;
  int c = a + b;
  printf("%d\n", c); // Adds a with b.

  int d, e, f;
  d = 34, e = 13, f = d - e;
  printf("%d\n", f); // Subtracts e from d.

  int g, h, i;
  g = 23, h = 34, i = g * h;
  printf("%d\n", i); // Multiplies g with h.

  int j, k, l;
  j = 34, k = 2, l = j / k;
  printf("%d\n", l); // Divides j by k.

  int m, n, o;
  m = 33, n = 2, o = m % n;
  printf("%d\n", o); // Gives remainder of m divided by n.
  
}